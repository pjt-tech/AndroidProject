package com.kye.recyclerview_ex2;

public class Item {

    int imageId;
    String title;

    public Item(int imageId, String title) {
        this.imageId = imageId;
        this.title = title;
    }

    public int getImageId() {
        return imageId;
    }

    public String getTitle() {
        return title;
    }
}
